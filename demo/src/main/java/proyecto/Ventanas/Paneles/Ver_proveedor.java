    private void b_update_empleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_update_empleActionPerformed
        // TODO add your handling code here:
         int cont = 0;
        if(t_nombre.getText().isEmpty() ){
            cont++;
            t_nombre.setBorder(new LineBorder(Color.red,1));
        }
        if(t_apellido.getText().isEmpty() ){
            cont++;
            t_apellido.setBorder(new LineBorder(Color.red,1));
        }
        if(t_id.getText().isEmpty() ){
            cont++;
            t_id.setBorder(new LineBorder(Color.red,1));
        }
        if(t_cp.getText().isEmpty() ){
            cont++;
            t_cp.setBorder(new LineBorder(Color.red,1));
        }
        

        if (cont == 0 && !error) {
            String selectedValue = cb_proveedor.getSelectedItem().toString();
            String id = selectedValue.split("-")[0];

            Main.updateProveedor(id, t_nombre.getText(), t_apellido.getText(), t_cp.getText());
            cb_proveedor.removeAllItems();
            Main.getProveedortoComboBox(cb_proveedor);
        }
        else{
            JOptionPane.showMessageDialog(null, "Revisa los datos");

        }
        
    }//GEN-LAST:event_b_update_empleActionPerformed

