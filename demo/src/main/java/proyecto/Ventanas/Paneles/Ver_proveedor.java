    private void cb_proveedorItemStateChanged(java.awt.event.ItemEvent evt) throws SQLException {//GEN-FIRST:event_cb_proveedorItemStateChanged
        // TODO add your handling code here:
        //db.get_empleados();
        if(cb_proveedor.getItemCount() > 0) {
            String selectedValue = cb_proveedor.getSelectedItem().toString();
            String id = selectedValue.split("-")[0];

            Proveedor p = Main.getIdProveedor(id);


            t_nombre.setText(p.getNombre());
            t_apellido.setText(p.getApellido());
            t_cp.setText(p.getDireccion());
            t_id.setText(p.getId());
            

            t_nombre.setEnabled(false);
            t_apellido.setEnabled(false);
            t_cp.setEnabled(false);
            t_id.setEnabled(false);
          
            b_update_emple.setEnabled(false);

        }


        
    }//GEN-LAST:event_cb_proveedorItemStateChanged

