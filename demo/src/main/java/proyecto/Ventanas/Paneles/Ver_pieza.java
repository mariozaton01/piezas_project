    private void cb_piezaItemStateChanged(java.awt.event.ItemEvent evt) throws SQLException {//GEN-FIRST:event_cb_piezaItemStateChanged
        // TODO add your handling code here:
        if(cb_pieza.getItemCount() > 0){
            String selectedValue = cb_pieza.getSelectedItem().toString();
            String id = selectedValue.split("-")[0];

            Pieza p = Main.getIdPieza(id);
            
            t_id.setText(p.getId());
            t_descripcion.setText(p.getDescripcion());
            t_nombre.setText(p.getNombre());
            n_precio.setValue(p.getPrecio());

            t_id.setEnabled(false);
            t_descripcion.setEnabled(false);
            n_precio.setEnabled(false);
            t_nombre.setEnabled(false);
            b_update_cliente.setEnabled(false);
        }

    }//GEN-LAST:event_cb_piezaItemStateChanged

