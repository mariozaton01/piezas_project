    private void b_update_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_update_clienteActionPerformed
        // TODO add your handling code here:
        int cont = 0;
        
        if(t_nombre.getText().isEmpty() ){
            cont++;
            t_nombre.setBorder(new LineBorder(Color.red,1));
        }
        if(t_descripcion.getText().isEmpty() ){
            cont++;
            t_descripcion.setBorder(new LineBorder(Color.red,1));
        }
        if(n_precio.getValue().toString().equals('0') ){
            cont++;
            n_precio.setBorder(new LineBorder(Color.red,1));
        }
        
        if (cont == 0) {
            String selectedValue = cb_pieza.getSelectedItem().toString();
            String id = selectedValue.split("-")[0];

            Main.updatePieza(id, t_nombre.getText(), t_descripcion.getText(),  Float.parseFloat(n_precio.getValue().toString() ) );
            cb_pieza.removeAllItems();
            Main.getPiezasstoComboBox(cb_pieza);
        }
        else{
            JOptionPane.showMessageDialog(null, "Revisa los datos");

        }
    }//GEN-LAST:event_b_update_clienteActionPerformed

