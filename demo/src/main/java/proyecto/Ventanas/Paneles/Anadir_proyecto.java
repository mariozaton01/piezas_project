    private void b_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_guardarActionPerformed
        // TODO add your handling code here:
        int cont = 0;
        if(t_id.getText().isEmpty() ){
            cont++;
            t_id.setBorder(new LineBorder(Color.red,1));
        }
        if(t_ciudad.getText().isEmpty() ){
            cont++;
            t_ciudad.setBorder(new LineBorder(Color.red,1));
        }
        if(t_nombre.getText().isEmpty() ){
            cont++;
            t_nombre.setBorder(new LineBorder(Color.red,1));
        }
        
        if (cont == 0){
            Proyectos proyecto = new Proyectos();
            proyecto.setId(t_id.getText());
            proyecto.setNombre(t_nombre.getText());
            proyecto.setCiudad(t_ciudad.getText());
            Main.insertProyecto(proyecto);
        }
        else{
            JOptionPane.showMessageDialog(null, "No dejes campos vacios");
        }
    }//GEN-LAST:event_b_guardarActionPerformed

