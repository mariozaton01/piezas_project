    private void b_anadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_anadirActionPerformed
        // TODO add your handling code here:
        int cont = 0;
        if(t_nombre.getText().isEmpty() ){
            cont++;
            t_nombre.setBorder(new LineBorder(Color.red,1));
        }
        if(t_apellido.getText().isEmpty() ){
            cont++;
            t_apellido.setBorder(new LineBorder(Color.red,1));
        }
        if(t_id.getText().isEmpty() ){
            cont++;
            t_id.setBorder(new LineBorder(Color.red,1));
        }
        if(t_cp.getText().isEmpty() ){
            cont++;
            t_cp.setBorder(new LineBorder(Color.red,1));
        }
        

        if (cont == 0 && !error) {
            Proveedor proveedor = new Proveedor();
            proveedor.setId(t_id.getText());
            proveedor.setNombre(t_nombre.getText());
            proveedor.setApellido(t_apellido.getText());
            proveedor.setDireccion(t_cp.getText());

            Main.insertProveedor(proveedor);
        }
        else{
            JOptionPane.showMessageDialog(null, "Revisa los datos");

        }
    }//GEN-LAST:event_b_anadirActionPerformed

