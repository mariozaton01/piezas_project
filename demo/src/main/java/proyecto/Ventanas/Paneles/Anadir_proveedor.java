    private void t_idFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_t_idFocusLost
        // TODO add your handling code here:
        String id = t_id.getText();
        Proveedor p = Main.getIdProveedor(id);
        if (p == null){
            error = false;
        }
        else {
            error = true;
        }
        if (error){
            t_id.setBorder(new LineBorder(Color.red,1));
            t_id.setText("DNI ya utilizado");
            t_id.setForeground(Color.red);

        }
        
    }//GEN-LAST:event_t_idFocusLost


