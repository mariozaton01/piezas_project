    private void b_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_guardarActionPerformed
        // TODO add your handling code here:
        String selectedPieza = cb_pieza.getSelectedItem().toString();
        String idPieza = selectedPieza.split("-")[0];
        
        String selectedProveedor = cb_proveedor.getSelectedItem().toString();
        String idProveedor = selectedProveedor.split("-")[0];
        
        String selectedProyecto = cb_proyecto.getSelectedItem().toString();
        String idProyecto = selectedProyecto.split("-")[0];
        
        Gestion existe = Main.getGestionByIds(idPieza, idProveedor, idProyecto);
        
        if (existe == null){
            if(Integer.parseInt(n_cantidad.getValue().toString()) >0){
                Gestion gestion = new Gestion();
                gestion.setIdPieza(idPieza);
                gestion.setIdProveedor(idProveedor);
                gestion.setIdProyecto(idProyecto);
                gestion.setCantidad(Integer.parseInt(n_cantidad.getValue().toString()) );
                Main.insertGestion(gestion);
            }
            else{
                JOptionPane.showMessageDialog(null, "Aumenta la cantidad seleccionada.");
            }

        }
        else{
            JOptionPane.showMessageDialog(null, "Esta gestion ya esta asociada");
        }
    }//GEN-LAST:event_b_guardarActionPerformed


