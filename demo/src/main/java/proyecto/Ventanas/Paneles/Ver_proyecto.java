    private void b_update_proyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_update_proyectoActionPerformed
        // TODO add your handling code here:
        int cont = 0;
        if (t_nombre.getText().isEmpty()) {
            cont++;
            t_nombre.setBorder(new LineBorder(Color.red, 1));
        }
        if (t_ciudad.getText().isEmpty()) {
            cont++;
            t_ciudad.setBorder(new LineBorder(Color.red, 1));
        }


        if (cont == 0) {
            String selectedValue = cb_proyecto.getSelectedItem().toString();
            String id = selectedValue.split("-")[0];

            Main.updateProyecto(id, t_nombre.getText(), t_ciudad.getText());
            cb_proyecto.removeAllItems();
            Main.getProyectotoComboBox(cb_proyecto);

        }



    }//GEN-LAST:event_b_update_proyectoActionPerformed

