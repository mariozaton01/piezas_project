    private void cb_proyectoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_proyectoItemStateChanged
        // TODO add your handling code here:
        //db.get_empleados();
        if(cb_proyecto.getItemCount() > 0) {
            String selectedValue = cb_proyecto.getSelectedItem().toString();
            String id = selectedValue.split("-")[0];
            
            Proyectos p = Main.getIdProyecto(id);

            t_nombre.setText(p.getNombre());
            t_ciudad.setText(p.getCiudad());
            t_id.setText(p.getId());

            t_nombre.setEnabled(false);
            t_ciudad.setEnabled(false);
            t_id.setEnabled(false);

            b_update_proyecto.setEnabled(false);

        }

    }//GEN-LAST:event_cb_proyectoItemStateChanged


